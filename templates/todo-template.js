/*
To Do: Create and export a function that accepts an object as a parameter and returns a string

Expected input:
{
  username: 'jsmith',
  todos: [
    { id: 1, task: 'go shopping' },
    { id: 2, task: 'walk the dog' },
    { id: 3, task: 'take out the garbage' }
  ]
}

Expected return value:
"<div>
  <h1>Todos for jsmith:</h1>
  <ul>
    <li>go shopping (1)</li>
    <li>walk the dog (2)</li>
    <li>take out the garbage (3)</li>
  </ul>
</div>"

Note: You don't need to worry about new lines or tabs. The return string can be all on one line.
*/
const todoTemplate = function(obj) {
  return `<div> 
  <h1>Todos for: ${obj.username}</h1>
  <ul>
    <li> ${obj.todos[0].task} </li> 
    <li> ${obj.todos[1].task} </li>
    <li>${obj.todos[2].task}</li>
  </ul>
  </div>`;
};

module.exports = todoTemplate;